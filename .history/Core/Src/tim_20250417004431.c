// 速度计算代码
/*
中断流程：
1.硬件中断触发：TIM3 计数器达到 Period 值，触发更新事件。
2.HAL 库中断处理：
  STM32 的中断控制器（NVIC）接收中断请求，调用 TIM3_IRQHandler 函数（在 stm32f1xx_it.c 中）。
  TIM3_IRQHandler 内部会调用 HAL_TIM_IRQHandler(&htim3)，该函数检测到更新事件后，调用 HAL_TIM_PeriodElapsedCallback(htim)。
3.用户回调函数：
  在 tim.c 中重写了 HAL_TIM_PeriodElapsedCallback，通过判断 htim->Instance == TIM3，执行电机速度计算逻辑。

*/
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM3) // 对应main函数中的tim3中断
  {
    static float encoder_angle_last = 0;

    /******encoder_angle_last默认值是0，不能用于计算，需要赋初值*********/
    static int once = 1;
    if (once)
    {
      once = !once;
      encoder_angle_last = encoder_angle;
    }
    /***************/

    float diff_angle = cycle_diff(encoder_angle - encoder_angle_last, 2 * PI);
    encoder_angle_last = encoder_angle;
    float _motor_speed = diff_angle * motor_speed_calc_freq; // 角度*电机速度计算频率
    float filter_alpha_speed = 0.07;                         // 低通滤波参数，如果希望保留更多的细节，可以增加alpha值
    motor_speed = low_pass_filter(_motor_speed, motor_speed, filter_alpha_speed);
  }
}

